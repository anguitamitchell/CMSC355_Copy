package monitoring;

public class Entry {
    private String username;
    private String date;
    private int carbs;
    private int glucose;
    private double insulinUnits;

    public String getUsername() {
        return username;
    }

    public String getDate() {
        return date;
    }

    public int getCarbs() {
        return carbs;
    }

    public int getGlucose() {
        return glucose;
    }

    public double getInsulinUnits() {
        return insulinUnits;
    }
}